package io.wisoft.semiar;

public class Employee {
    private String emp_code;
    private String emp_name;
    private String emp_mgt;
    private Integer emp_sal;
    private String emp_rcode;

    public String getEmp_code() {
        return emp_code;
    }

    public void setEmp_code(String emp_code) {
        this.emp_code = emp_code;
    }

    public String getEmp_name() {
        return emp_name;
    }

    public void setEmp_name(String emp_name) {
        this.emp_name = emp_name;
    }

    public String getEmp_mgt() {
        return emp_mgt;
    }

    public void setEmp_mgt(String emp_mgt) {
        this.emp_mgt = emp_mgt;
    }

    public Integer getEmp_sal() {
        return emp_sal;
    }

    public void setEmp_sal(Integer emp_sal) {
        this.emp_sal = emp_sal;
    }

    public String getEmp_rcode() {
        return emp_rcode;
    }

    public void setEmp_rcode(String emp_rcode) {
        this.emp_rcode = emp_rcode;
    }
}
